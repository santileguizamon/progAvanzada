import java.util.Date;

public class Vuelo {

	public String nombreAvion;
	public String nombreDestino;
	public Date horarioSalidaLlegada;
	public Date fecha;
	
	
	
	
	public String getNombreAvion() {
		return nombreAvion;
	}


	public void setNombreAvion(String nombreAvion) {
		this.nombreAvion = nombreAvion;
	}


	public String getNombreDestino() {
		return nombreDestino;
	}


	public void setNombreDestino(String nombreDestino) {
		this.nombreDestino = nombreDestino;
	}


	public Date getHorarioSalidaLlegada() {
		return horarioSalidaLlegada;
	}


	public void setHorarioSalidaLlegada(Date horarioSalidaLlegada) {
		this.horarioSalidaLlegada = horarioSalidaLlegada;
	}


	public Date getFecha() {
		return fecha;
	}


	public void setFecha(Date fecha) {
		this.fecha = fecha;
	}





	
}
